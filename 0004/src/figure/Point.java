public class Point {
    private int x;
    private int y;
    public Point(int x, int y)
    {
        this.x = x;
        this.y = y;
    }
    public int getX(Point point)
    {

        return this.x;
    }
    public int getY(Point point)
    {
        return this.y;
    }
}
